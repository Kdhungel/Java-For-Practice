/*
 * FINAl
 * 1. final is the keyword and access modifier which is used to apply
 * restrictions on a class, method or variable.
 * 
 * 2. Final keyword is used with the classes, methods and variables.
 * 
 * 3. (1) Once declared, final variable becomes constant and cannot be modified.
 * (2) final method cannot be overridden by sub class.
 * (3) final class cannot be inherited.
 * 
 * 4. Final method is executed only when we call it.
 * 
 */


 /*
  * FINALLY
  * 1. finally is the block in Java Exception Handling to execute the important
  * code whether the exception occurs or not.
  * 2. Finally block is always related to the try and catch block in exception
  * handling.
  * 3. (1) finally block runs the important code even if exception occurs or not.
  * (2) finally block cleans up all the resources used in try block
  * 
  * 4. Finally block is executed as soon as the try-catch block is executed.
  * 5. It's execution is not dependant on the exception.
  */

